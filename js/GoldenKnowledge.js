window.getKnowledgeByTerm = (term) => {
  if (!term) return null;
  return window.GoldenKnowledge.find(k => 
    k.term === term || 
    k.reading === term || 
    k.english.toLowerCase() === term.toLowerCase() || 
    (term.length > 2 && k.term.includes(term))
  );
};

window.getRandomNeoInterpretation = (term) => {
  const knowledge = window.getKnowledgeByTerm(term);
  if (!knowledge || !knowledge.neoInterpretations || knowledge.neoInterpretations.length === 0) return null;
  const randomIndex = Math.floor(Math.random() * knowledge.neoInterpretations.length);
  return knowledge.neoInterpretations[randomIndex];
};
